function show_hide_1() {
    var x = document.getElementById("section_content_1");
    if (x.style.display === "none") {
      x.style.display = "block";
      document.getElementById("show_hide_1").innerHTML = "[Hide]";
    } else {
      x.style.display = "none";
      document.getElementById("show_hide_1").innerHTML = "[Show]";
    }
  }

